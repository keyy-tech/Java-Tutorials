        int number = 2;

        System.out.println(number);
        // Assignment convert to upper/lower case
        String hostel = "hostel";
        System.out.println(hostel.toUpperCase());
        System.out.println(hostel.toLowerCase());

        // Assignment to student charAt
        String check = "abc";
        System.out.println(check.charAt(1));

